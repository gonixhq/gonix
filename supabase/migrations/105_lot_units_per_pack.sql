-- 105: เก็บ "ยูนิตต่อขวด/แพ็ค" ระดับล็อต (รองรับขวดหลายขนาด เช่น Botox 100/200u)
-- ════════════════════════════════════════════════════════════
-- inventory.units_per_pack (mig 097) = ขนาดแพ็คเริ่มต้นของ item (fixed)
-- แต่ Botox ขวดมีหลายขนาด (100u / 200u) → ต้องเก็บขนาดต่อ "ล็อต" ที่รับเข้าแต่ละครั้ง
--   - ตอนรับเข้า: จำนวนขวด × ยูนิต/ขวด = ยูนิตรวม (เก็บ stock เป็นยูนิต)
--   - แสดงผล: เหลือกี่ยูนิต + ประมาณกี่ขวด (qty_remaining / units_per_pack) ต่อล็อต
-- ════════════════════════════════════════════════════════════

ALTER TABLE inventory_lots ADD COLUMN IF NOT EXISTS units_per_pack numeric;
COMMENT ON COLUMN inventory_lots.units_per_pack IS 'ยูนิตต่อขวด/แพ็คของล็อตนี้ (เช่น 100 หรือ 200 u/ขวด) — ระบุตอนรับเข้า';
